package com.obss.radar.searcher.po;

public class Statistics {
	private String date;
	private String recordCount;
	private String keywordPageCount;
	private String totalRecordCount;
	private String totalKeywordPageCount;
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getRecordCount() {
		return recordCount;
	}
	public void setRecordCount(String recordCount) {
		this.recordCount = recordCount;
	}
	public String getKeywordPageCount() {
		return keywordPageCount;
	}
	public void setKeywordPageCount(String keywordPageCount) {
		this.keywordPageCount = keywordPageCount;
	}
	public String getTotalRecordCount() {
		return totalRecordCount;
	}
	public void setTotalRecordCount(String totalRecordCount) {
		this.totalRecordCount = totalRecordCount;
	}
	public String getTotalKeywordPageCount() {
		return totalKeywordPageCount;
	}
	public void setTotalKeywordPageCount(String totalKeywordPageCount) {
		this.totalKeywordPageCount = totalKeywordPageCount;
	}
	
}
